class AuthAuditTests(APITestCase):
    """Verify audit entries for signup, login, logout, tenant switch."""

    def setUp(self):
        cache.clear()
        self.tenant, self.groups = _setup_tenant_with_groups()
        self.user = _create_user(email="auth@gap.example.com")
        self.member = _create_membership(self.user, self.tenant)
        login_as(self.client, self.user, self.tenant)
        AuditLog.objects.all().delete()

    def test_login_failed_creates_audit_entry(self):
        self.client.logout()
        resp = self.client.post("/api/auth/login/", {
            "email": "auth@gap.example.com",
            "password": "WrongPassword!1",
        }, format="json")
        self.assertIn(resp.status_code, [400, 401])

        log = AuditLog.objects.get(
            resource_type="auth", action=AuditAction.LOGIN_FAILED,
        )
        self.assertEqual(log.resource_repr, "Login failed: auth@gap.example.com")
        self.assertIsNone(log.actor)
        self.assertEqual(log.actor_email, "")

    def test_login_failed_returns_usable_error_message(self):
        """Wrong password response should contain the actual error, not
        a generic 'Validation error.' with a request ID."""
        resp = self.client.post("/api/auth/login/", {
            "email": "auth@gap.example.com",
            "password": "WrongPassword!1",
        }, format="json")
        data = resp.json()
        # The message field should carry the real error
        self.assertIn("Invalid email or password", data["message"])

    def test_login_failed_audit_visible_in_list_api(self):
        """Failed login audit entries should be visible when querying
        the audit list API for the same tenant."""
        # Trigger a failed login (no tenant header — pre-auth)
        self.client.post("/api/auth/login/", {
            "email": "auth@gap.example.com",
            "password": "WrongPassword!1",
        }, format="json")

        # Verify entry was written to DB
        self.assertTrue(
            AuditLog.objects.filter(action=AuditAction.LOGIN_FAILED).exists(),
            "Audit entry should exist in the database",
        )

        # Query the audit list API as admin
        resp = self.client.get(
            "/api/audit/",
        )
        self.assertEqual(resp.status_code, 200)
        data = resp.json()
        login_failed = [
            e for e in data["results"] if e["action"] == "login_failed"
        ]
        self.assertEqual(
            len(login_failed), 1,
            "Failed login audit entry should appear in the tenant audit list",
        )

    def test_login_success_creates_audit_entry(self):
        # Use a MEMBER (not OWNER) to avoid MFA gate blocking full login
        member_user = User.objects.create_user(
            email="loginok@gap.example.com", password=STRONG_PASSWORD,
            email_verified=True,
        )
        TenantMember.objects.create(
            tenant=self.tenant, user=member_user, role=TenantRole.MEMBER, is_active=True,
        )
        self.client.logout()
        resp = self.client.post("/api/auth/login/select-tenant/", {
            "email": "loginok@gap.example.com",
            "password": STRONG_PASSWORD,
            "tenant_id": str(self.tenant.id),
        }, format="json")
        self.assertEqual(resp.status_code, 200)

        log = AuditLog.objects.get(
            resource_type="auth", action=AuditAction.LOGIN_SUCCESS,
        )
        self.assertIn("loginok@gap.example.com", log.resource_repr)
        self.assertIn(self.tenant.name, log.resource_repr)
        self.assertEqual(log.tenant, self.tenant)

    def test_logout_creates_audit_entry(self):
        resp = self.client.post(
            "/api/auth/logout/",
            {},
            format="json",
        )
        self.assertEqual(resp.status_code, 204)

        log = AuditLog.objects.get(
            resource_type="auth", action=AuditAction.LOGOUT,
        )
        self.assertEqual(log.resource_repr, f"Logout: {self.user.email}")
        self.assertEqual(log.tenant, self.tenant)

    def test_switch_tenant_creates_audit_entry(self):
        # Create a second tenant + membership for the same user
        tenant2 = Tenant.objects.create(name="Second Corp", slug="second-corp")
        create_default_groups_for_tenant(tenant2)
        TenantMember.objects.create(
            tenant=tenant2, user=self.user, role=TenantRole.MEMBER,
        )
        AuditLog.objects.all().delete()

        resp = self.client.post(
            "/api/auth/switch-tenant/",
            {"tenant_id": str(tenant2.id)},
            format="json",
        )
        self.assertEqual(resp.status_code, 200)

        log = AuditLog.objects.get(
            resource_type="auth", action=AuditAction.TENANT_SWITCH,
        )
        self.assertEqual(log.resource_repr, f"Tenant switch: {tenant2.id}")


