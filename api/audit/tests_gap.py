class GroupManagementAuditTests(APITestCase):
    """Verify audit entries for group CRUD and member add/remove."""

    def setUp(self):
        self.tenant, self.groups = _setup_tenant_with_groups()
        self.user = _create_user(email="grpmgmt@gap.example.com")
        self.member = _create_membership(self.user, self.tenant)
        login_as(self.client, self.user, self.tenant)
        AuditLog.objects.all().delete()

    def _api(self, method, url, data=None):
        fn = getattr(self.client, method)
        kwargs = {}
        if data is not None:
            return fn(url, data, format="json", **kwargs)
        return fn(url, **kwargs)

    def test_group_create_creates_audit_entry(self):
        resp = self._api("post", "/api/authorization/groups/", {
            "name": "Pentesters",
            "permission_ids": [],
        })
        self.assertEqual(resp.status_code, 201)

        log = AuditLog.objects.get(resource_type="group", action=AuditAction.CREATE)
        self.assertEqual(log.resource_repr, "Group: Pentesters")
        self.assertIsNone(log.before)
        self.assertIsNotNone(log.after)

    def test_group_update_creates_audit_entry(self):
        # Create a custom group first
        resp = self._api("post", "/api/authorization/groups/", {
            "name": "Updatable",
            "permission_ids": [],
        })
        gid = resp.json()["id"]
        AuditLog.objects.all().delete()

        resp = self._api("patch", f"/api/authorization/groups/{gid}/", {
            "name": "Renamed",
        })
        self.assertEqual(resp.status_code, 200)

        log = AuditLog.objects.get(resource_type="group", action=AuditAction.UPDATE)
        self.assertIsNotNone(log.before)
        self.assertIsNotNone(log.after)
        self.assertIsNotNone(log.diff)
        self.assertIn("name", log.diff)

    def test_group_delete_creates_audit_entry(self):
        resp = self._api("post", "/api/authorization/groups/", {
            "name": "Doomed Group",
            "permission_ids": [],
        })
        gid = resp.json()["id"]
        AuditLog.objects.all().delete()

        resp = self._api("delete", f"/api/authorization/groups/{gid}/")
        self.assertEqual(resp.status_code, 204)

        log = AuditLog.objects.get(resource_type="group", action=AuditAction.DELETE)
        self.assertIsNotNone(log.before)
        self.assertIsNone(log.after)
        self.assertEqual(log.resource_repr, "Group: Doomed Group")

    def test_group_member_add_creates_audit_entry(self):
        # Create a custom group
        resp = self._api("post", "/api/authorization/groups/", {
            "name": "AddTest",
            "permission_ids": [],
        })
        gid = resp.json()["id"]

        # Create a second user to add
        user2 = _create_user(email="target@gap.example.com")
        member2 = _create_membership(
            user2, self.tenant, role=TenantRole.MEMBER,
        )
        AuditLog.objects.all().delete()

        resp = self._api(
            "post",
            f"/api/authorization/groups/{gid}/members/",
            {"member_id": str(member2.pk)},
        )
        self.assertEqual(resp.status_code, 201)

        log = AuditLog.objects.get(resource_type="group", action=AuditAction.UPDATE)
        self.assertIn("Added member", log.resource_repr)
        self.assertIn("target@gap.example.com", log.resource_repr)

    def test_group_member_remove_creates_audit_entry(self):
        # Create a custom group
        resp = self._api("post", "/api/authorization/groups/", {
            "name": "RemoveTest",
            "permission_ids": [],
        })
        gid = resp.json()["id"]

        # Create a second user and add to group
        user2 = _create_user(email="removable@gap.example.com")
        member2 = _create_membership(
            user2, self.tenant, role=TenantRole.MEMBER,
        )
        self._api(
            "post",
            f"/api/authorization/groups/{gid}/members/",
            {"member_id": str(member2.pk)},
        )
        AuditLog.objects.all().delete()

        resp = self._api(
            "delete",
            f"/api/authorization/groups/{gid}/members/{member2.pk}/",
        )
        self.assertEqual(resp.status_code, 204)

        log = AuditLog.objects.get(resource_type="group", action=AuditAction.UPDATE)
        self.assertIn("Removed member", log.resource_repr)
        self.assertIn("removable@gap.example.com", log.resource_repr)

    def test_default_group_update_rejected_no_audit(self):
        # Pick a default group
        from authorization.models import TenantGroup
        default_group = TenantGroup.objects.filter(
            tenant=self.tenant, is_default=True,
        ).first()
        self.assertIsNotNone(default_group)

        resp = self._api("patch", f"/api/authorization/groups/{default_group.pk}/", {
            "name": "Hacked",
        })
        self.assertEqual(resp.status_code, 403)
        self.assertEqual(
            AuditLog.objects.filter(resource_type="group").count(), 0,
        )


