class FindingReadAuditTests(APITestCase):
    """Verify audit entry for retrieving a single finding (sensitive read)."""

    def setUp(self):
        self.tenant, self.groups = _setup_tenant_with_groups()
        self.user = _create_user(email="findread@gap.example.com")
        self.member = _create_membership(self.user, self.tenant)
        login_as(self.client, self.user, self.tenant)

        from clients.models import Client
        from assets.models import Asset

        org = Client.objects.create(tenant=self.tenant, name="FindCo")
        self.asset = Asset.objects.create(
            tenant=self.tenant, client=org, name="webapp",
            asset_type="webapp",
        )

        # Create engagement via API (auto-creates Sow)
        resp = self.client.post(
            "/api/engagements/",
            {"name": "FindEng", "status": "active", "client_id": str(org.pk)},
            format="json",
        )
        self.assertEqual(resp.status_code, 201)
        self.eng_id = resp.json()["id"]

        # Add asset to scope
        self.client.post(
            f"/api/engagements/{self.eng_id}/scope/",
            {"asset_id": str(self.asset.pk)},
            format="json",
        )

        # Approve SoW so findings can be created
        self.client.patch(
            f"/api/engagements/{self.eng_id}/sow/",
            {"status": "approved"},
            format="json",
        )

        # Create finding
        resp = self.client.post(
            f"/api/engagements/{self.eng_id}/findings/",
            {"title": "XSS Gap", "severity": "high", "asset_id": str(self.asset.pk)},
            format="json",
        )
        self.assertEqual(resp.status_code, 201)
        self.finding_id = resp.json()["id"]

        AuditLog.objects.all().delete()

    def test_finding_retrieve_creates_read_audit(self):
        resp = self.client.get(
            f"/api/engagements/{self.eng_id}/findings/{self.finding_id}/",
        )
        self.assertEqual(resp.status_code, 200)

        log = AuditLog.objects.get(resource_type="finding", action=AuditAction.READ)
        self.assertEqual(log.resource_id, str(self.finding_id))
        self.assertEqual(log.resource_repr, "Finding: XSS Gap")


